#include <iostream>

int main() {
    // Usamos un bucle for para imprimir los números del 1 al 100.
    for (int i = 1; i <= 100; i++) {
        std::cout << i << " ";
    }

    // Agregamos un salto de línea al final para que la salida se vea ordenada.
    std::cout << std::endl;

    return 0;
}
